# Calculation of Two Dimensional Numpy
import numpy as np
a = np.array([[1,2],[3,4]])
b = np.array([[2,2],[2,2]])
print("Add : ", a + b)
print("Sub : ", a - b)
print("Mul : ", a * b)
print("Div : ", a / b)
print("a + 1 : ", a + 1)
print("Dot Product : ", np.dot(a,b)) # numpy dot product
print("Mean : ", np.mean(a)) 
print("Max  : ", np.max(a))
